package com.morepractice;

public class PositiveNegativer {

    public static void main(String[] args) {

        double number = -1;

        if(number < 0.0) {
            System.out.println(number + " is negative");
        } else if(number > 0.0) {
            System.out.println(number + " is positive");
        } else {
            System.out.println(number + " is zero");
        }
    }
}
